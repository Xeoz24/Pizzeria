    private void reitrarBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_reitrarBtnActionPerformed
        // TODO add your handling code here:
        if (!"0".equals(retirartxt.getText()) && !"".equals(retirartxt.getText())) {
            double retiro = Double.parseDouble(retirartxt.getText());
            if (retiro > 0) {
                if (cliente.ConsultarSaldo() >= retiro) {
                    historialMovimiento.setText("SE RETIRO: $ " + retiro);
                    cliente.Retirar(retiro);
                    saldo.setText("$ " + String.valueOf(cliente.ConsultarSaldo()));
                    contadorMovimientos++;
                    movimientoRealizado.setText("Movimientos Realizados " + contadorMovimientos);
                    JOptionPane.showMessageDialog(null, "SE HA RETIRADO CON EXITO!", "AVISO", JOptionPane.INFORMATION_MESSAGE);
                } else {
                    JOptionPane.showMessageDialog(null, "NO TIENES SALDO SUFICIENTE", "ERROR", JOptionPane.ERROR_MESSAGE);
                }
            } else {
                JOptionPane.showMessageDialog(null, "NO PUEDE INGRESAR CANTIDAD NEGATIVA", "ERROR", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(null, "INGRESE UNA CANTIDAD", "ERROR", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_reitrarBtnActionPerformed

