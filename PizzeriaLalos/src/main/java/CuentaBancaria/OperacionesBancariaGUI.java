    private void ingresarBtnActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ingresarBtnActionPerformed
        // TODO add your handling code here:
        if (!"0".equals(depositotxt.getText()) && !"".equals(depositotxt.getText())) {
            double deposito = Double.parseDouble(depositotxt.getText());
            if (deposito > 0) {
                historialMovimiento.setText("SE DEPSITO: $ " + deposito);
                cliente.Depositar(deposito);
                saldo.setText("$ " + String.valueOf(cliente.ConsultarSaldo()));
                contadorMovimientos++;
                movimientoRealizado.setText("Movimientos Realizados " + contadorMovimientos);
                JOptionPane.showMessageDialog(null, "SE HA INGRESADO CON EXITO!", "AVISO", JOptionPane.INFORMATION_MESSAGE);
            } else {
                JOptionPane.showMessageDialog(null, "NO PUEDE INGRESAR CANTIDAD NEGATIVA", "ERROR", JOptionPane.ERROR_MESSAGE);
            }
        } else {
            JOptionPane.showMessageDialog(null, "INGRESE UNA CANTIDAD", "ERROR", JOptionPane.ERROR_MESSAGE);
        }
    }//GEN-LAST:event_ingresarBtnActionPerformed

